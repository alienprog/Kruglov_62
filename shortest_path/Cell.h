/**
 * @file Cell.h
 * @brief Класс клетки лабиринта.
 *
 * Хранит информацию о типе клетки (стена/проход), состоянии для BFS,
 * расстоянии от старта, родительской клетке и координатах.
 */

#pragma once

 /**
  * @class Cell
  * @brief Клетка лабиринта.
  */
class Cell {
public:
    bool  isWall;      ///< true – стена, false – проход
    bool  visited;     ///< true – клетка посещена при BFS
    int   distance;    ///< расстояние от старта
    Cell* parent;      ///< указатель на родительскую клетку (для восстановления пути)
    int   x;           ///< столбец
    int   y;           ///< строка

    /**
     * @brief Конструктор по умолчанию.
     *
     * Инициализирует клетку как проход, непосещённую,
     * с нулевым расстоянием, без родителя и координатами (0,0).
     */
    Cell();

    /**
     * @brief Сброс состояния клетки для нового поиска пути.
     *
     * Очищает флаг посещения, обнуляет расстояние и указатель на родителя.
     */
    void reset();
};